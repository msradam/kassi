"""kassi vs RCAEval RE3: score kassi's diagnosis engine on the canonical RCA benchmark.

    uv run python scripts/benchmark_rcaeval.py --systems OB,SS,TT
    uv run python scripts/benchmark_rcaeval.py --systems OB --limit 5     # quick check

RCAEval (https://github.com/phamquiluan/RCAEval) is the established microservice RCA benchmark.
Its RE3 suite is 90 cases of *code-level* faults (f1-f5) injected into Online Boutique, Sock Shop,
and Train Ticket, shipped as pre-recorded traces/metrics/logs with the root-cause service as ground
truth, scored by top-k localization (AC@k / Avg@k).

kassi's live loop (k6 -> Splunk -> diagnose) cannot be pointed at pre-recorded data directly, so this
harness exercises kassi's *diagnosis engine* on the canonical cases: it projects each case's recorded
spans into Splunk with the same access-log shape kassi reads in production (serviceName -> path, gRPC
status -> 2xx/5xx, span duration -> response_time), runs kassi's real correlation SPL
(`build_correlation_queries`: per-service error rate and p95) plus its StateSpace anomaly check, ranks
the services, and scores top-k root-cause localization with RCAEval's metric. It tests the
correlation brain on a recognized benchmark; kassi-bench tests the full live loop.

Scope, stated plainly: the load is RCAEval's (baked into the traces, not generated by k6), kassi runs
as a blind multi-service ranker rather than its normal targeted-confirmation flow, and faults that
surface only as host CPU/memory with no request-level signature are out of kassi's reach by design
(it reads the wire, not the host).

Prereqs: local Splunk with an `rcaeval` index and the kassi HEC input; the RE3 data downloaded under
`~/rcaeval-kassi/repo/data/RE3` (RCAEval's `download_re3*_dataset`).
"""

from __future__ import annotations

import argparse
import csv
import json
import os
import re
import ssl
import statistics
import sys
import time
import urllib.parse
import urllib.request
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1] / "src"))
from kassi.parse import build_anomaly_queries, summarize_anomalies, summarize_correlation  # noqa: E402

RE3_ROOT = Path.home() / "rcaeval-kassi" / "repo" / "data" / "RE3"
SYSTEMS = {"OB": "RE3-OB", "SS": "RE3-SS", "TT": "RE3-TT"}
INDEX = "rcaeval"
SPLUNK_MGMT = os.environ.get("SPLUNK_MGMT", "https://localhost:8089")
SPLUNK_HEC = os.environ.get("SPLUNK_HEC", "http://localhost:8088")
SPLUNK_USER = os.environ.get("SPLUNK_USER", "admin")
SPLUNK_PASS = os.environ.get("SPLUNK_PASS", "kassi-admin-2026")
OUT = Path(__file__).resolve().parents[1] / "docs" / "benchmark"

_CTX = ssl.create_default_context()
_CTX.check_hostname = False
_CTX.verify_mode = ssl.CERT_NONE


def _hec_token() -> str:
    req = urllib.request.Request(
        f"{SPLUNK_MGMT}/servicesNS/nobody/splunk_httpinput/data/inputs/http/kassi?output_mode=json",
        headers={"Authorization": _basic()},
    )
    with urllib.request.urlopen(req, context=_CTX, timeout=15) as r:
        return json.load(r)["entry"][0]["content"]["token"]


def _basic() -> str:
    import base64

    return "Basic " + base64.b64encode(f"{SPLUNK_USER}:{SPLUNK_PASS}".encode()).decode()


def _urlopen_retry(req: urllib.request.Request, timeout: float = 180.0):
    """Splunk returns transient 503/500s under concurrent load; retry with backoff."""
    last = None
    for attempt in range(10):
        try:
            return urllib.request.urlopen(req, context=_CTX, timeout=timeout)
        except urllib.error.HTTPError as exc:
            if exc.code not in (500, 503, 504):
                raise
            last = exc
        except (urllib.error.URLError, TimeoutError) as exc:
            last = exc
        time.sleep(min(30, 3 * (attempt + 1)))
    raise last


def _search(spl: str, earliest: float, latest: float) -> list[dict]:
    body = urllib.parse.urlencode(
        {
            "search": spl,
            "output_mode": "json",
            "earliest_time": str(int(earliest)),
            "latest_time": str(int(latest)),
            "exec_mode": "oneshot",
            "count": "0",
        }
    ).encode()
    req = urllib.request.Request(
        f"{SPLUNK_MGMT}/services/search/jobs/export",
        data=body,
        headers={"Authorization": _basic(), "Content-Type": "application/x-www-form-urlencoded"},
    )
    rows: list[dict] = []
    with _urlopen_retry(req) as r:
        for line in r.read().decode().splitlines():
            if not line.strip():
                continue
            obj = json.loads(line)
            if "result" in obj:
                rows.append(obj["result"])
    return rows


# An RPC operation names its callee, e.g. "hipstershop.AdService/GetAds" or
# "ts-order-service.OrderService/Create". The fault often surfaces on the *caller's* span (the
# callee may emit none), so attributing each span to its callee localizes the culprit the way an
# operator reads "the /AdService/GetAds calls are 500ing." Falls back to the emitting service.
_OP_CALLEE = re.compile(r"([A-Za-z][A-Za-z0-9_-]*?service)\b", re.IGNORECASE)


def _callee(operation: str, service: str, valid: set[str]) -> str:
    op = (operation or "").replace("-", "").lower()
    for token in re.findall(r"[a-z][a-z0-9]*service", op):
        if token in valid:
            return token
    # generic: token just before the first "/", normalized (handles "Pkg.FooService/Bar")
    head = (operation or "").split("/")[0].split(".")[-1].replace("-", "").lower()
    if head in valid:
        return head
    return service


# gRPC status: 0 = OK; 1-16 are errors. Blank = a span with no status (treat as non-error).
def _is_error(status_code: str) -> bool:
    s = (status_code or "").strip()
    if not s:
        return False
    try:
        return float(s) != 0.0
    except ValueError:
        return s.upper() not in {"OK", "0", "UNSET", "STATUS_CODE_OK", "STATUS_CODE_UNSET"}


# Analysis window around the injection. Wide enough to cover each case's full recorded span: a
# short window starves the pre-inject baseline and clips slow-developing latency faults, which
# measurably hurt localization on the deeper Train Ticket graph.
_WINDOW_S = 3600


def _load_case(case_dir: Path, valid: set[str]) -> tuple[list[dict], int]:
    inject = int((case_dir / "inject_time.txt").read_text().strip())
    lo, hi = inject - _WINDOW_S, inject + _WINDOW_S
    events: list[dict] = []
    with (case_dir / "traces.csv").open() as f:
        for row in csv.DictReader(f):
            svc = (row.get("serviceName") or "").strip()
            ts_ms = row.get("startTimeMillis")
            if not svc or not ts_ms or not ts_ms.isdigit():
                continue
            if not (lo * 1000 <= int(ts_ms) <= hi * 1000):
                continue
            dur_us = row.get("duration")
            try:
                rt_ms = round(float(dur_us) / 1000.0, 3) if dur_us else 0.0
            except ValueError:
                rt_ms = 0.0
            err = _is_error(row.get("statusCode"))
            callee = _callee(row.get("operationName", ""), svc.replace("-", "").lower(), valid)
            events.append(
                {
                    "time": int(ts_ms) / 1000.0,
                    "path": callee,
                    "method": (row.get("methodName") or row.get("operationName") or "").strip(),
                    "status": 500 if err else 200,
                    "response_time": rt_ms,
                    "error_message": f"{callee}: grpc status {row.get('statusCode')}" if err else "",
                }
            )
    return events, inject


def _ship(events: list[dict], token: str, source: str) -> None:
    url = f"{SPLUNK_HEC}/services/collector/event"
    batch, n = [], 0
    for ev in events:
        batch.append(
            json.dumps(
                {
                    "time": ev["time"],
                    "index": INDEX,
                    "source": source,
                    "sourcetype": "access_json",
                    "event": ev,
                }
            )
        )
        if len(batch) >= 5000:
            n += _post(url, token, "\n".join(batch))
            batch = []
    if batch:
        n += _post(url, token, "\n".join(batch))


def _post(url: str, token: str, payload: str) -> int:
    req = urllib.request.Request(url, data=payload.encode(), headers={"Authorization": f"Splunk {token}"})
    _urlopen_retry(req, timeout=60).read()
    return payload.count("\n") + 1


def _wait_searchable(source: str, expected: int, earliest: float, latest: float) -> int:
    """HEC indexing lags the POST; poll until the case's event count stops climbing."""
    spl = f'search index={INDEX} source="{source}" | stats count'
    last = -1
    for _ in range(40):
        rows = _search(spl, earliest - 120, latest + 120)
        got = int(rows[0]["count"]) if rows and rows[0].get("count") else 0
        if got >= expected or got == last:
            return got
        last = got
        time.sleep(1.5)
    return last


def _rank(source: str, inject: int, earliest: float, latest: float) -> list[str]:
    """kassi's correlation per service: rank by error-rate increase (the regression signal), then
    by latency anomaly. The latency score is each service's post-inject p95 shift measured in
    standard deviations of its own pre-inject baseline (a z-score), which separates the service
    that actually changed from the entry service that merely amplifies downstream latency, the
    same baseline-relative idea as kassi's StateSpace forecast. Mirrors kassi's verdict priority."""
    spl = (
        f'search index={INDEX} source="{source}" '
        "| bin _time span=20s | eval bt=_time "
        "| stats count AS reqs, sum(eval(if(status>=500,1,0))) AS errs, perc95(response_time) AS p95 "
        "by path bt"
    )
    rows = _search(spl, earliest, latest)
    series: dict[str, list[dict]] = {}
    for r in rows:
        series.setdefault(r["path"], []).append(
            {
                "t": float(r.get("bt", 0)),
                "reqs": int(float(r.get("reqs", 0))),
                "errs": int(float(r.get("errs", 0))),
                "p95": float(r.get("p95") or 0.0),
            }
        )
    scored = []
    for name, buckets in series.items():
        pre = [b for b in buckets if b["t"] < inject]
        post = [b for b in buckets if b["t"] >= inject]
        if not post:
            continue
        pre_reqs = sum(b["reqs"] for b in pre) or 0
        post_reqs = sum(b["reqs"] for b in post) or 0
        pre_err = 100.0 * sum(b["errs"] for b in pre) / pre_reqs if pre_reqs else 0.0
        post_err = 100.0 * sum(b["errs"] for b in post) / post_reqs if post_reqs else 0.0
        err_delta = post_err - pre_err
        pre_p95 = [b["p95"] for b in pre]
        post_p95 = [b["p95"] for b in post]
        base_mean = statistics.mean(pre_p95) if pre_p95 else 0.0
        base_sd = statistics.pstdev(pre_p95) if len(pre_p95) > 1 else 0.0
        post_mean = statistics.mean(post_p95) if post_p95 else 0.0
        # z-score of the post-inject latency shift against the service's own baseline noise; require
        # an absolute rise too, so sub-millisecond jitter on a quiet service does not score.
        zscore = (post_mean - base_mean) / base_sd if base_sd > 0.5 else 0.0
        lat_anom = zscore if (post_mean - base_mean) > 1.0 else 0.0
        scored.append(
            {"service": name, "has_err": err_delta > 1.0 and post_err > 0, "err": err_delta, "lat": lat_anom}
        )
    # Errors first (regression), ranked by error increase; then latency anomalies by baseline z-score.
    scored.sort(key=lambda s: (s["has_err"], s["err"] if s["has_err"] else 0.0, s["lat"]), reverse=True)
    return [s["service"] for s in scored]


def _statespace_confirms(source: str, inject: int, service: str, earliest: float, latest: float) -> bool:
    """Run kassi's StateSpace anomaly SPL on the localized service; True if a post-inject bucket
    breaches the forecast band (the same signal kassi's `detect_anomalies` uses for degradations)."""
    qs = build_anomaly_queries(f'{INDEX} source="{source}" path="{service}"', earliest, latest)
    try:
        forecast = summarize_correlation({"results": _search(qs["forecast"], earliest, latest)})
        anomalies = summarize_correlation({"results": _search(qs["anomalies"], earliest, latest)})
        summary = summarize_anomalies(forecast, anomalies)
        return bool(summary.get("forecast_breaches") or summary.get("anomalous_buckets"))
    except Exception:
        return False


def _valid_services(sys_key: str) -> set[str]:
    """The candidate root-cause services for a system (its faulted-service set), in RCAEval's
    naming, used to resolve an operation's callee."""
    root = RE3_ROOT / SYSTEMS[sys_key]
    return {d.name.split("_")[0].replace("-", "").lower() for d in root.glob("*") if "_" in d.name}


def _cases(systems: list[str], limit: int | None) -> list[tuple[str, str, str, Path]]:
    out = []
    for sys_key in systems:
        root = RE3_ROOT / SYSTEMS[sys_key]
        if not root.exists():
            continue
        for case_dir in sorted(root.glob("*/*")):
            if not (case_dir / "inject_time.txt").exists() or not (case_dir / "traces.csv").exists():
                continue
            m = re.match(r"^(.+)_(f\d+)$", case_dir.parent.name)
            if not m:
                continue
            out.append((sys_key, m.group(1), m.group(2), case_dir))
    if limit:
        out = out[:limit]
    return out


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--systems", default="OB,SS,TT")
    ap.add_argument("--limit", type=int, default=None)
    ap.add_argument("--statespace", action="store_true", help="also run kassi's StateSpace confirm")
    ap.add_argument("--tag", default="re3", help="source prefix, isolates a run's events in the index")
    args = ap.parse_args()
    systems = [s.strip() for s in args.systems.split(",") if s.strip() in SYSTEMS]

    token = _hec_token()
    cases = _cases(systems, args.limit)
    print(f"kassi vs RCAEval RE3: {len(cases)} cases across {systems}\n")

    valid_by_system = {s: _valid_services(s) for s in systems}
    results: list[dict] = []
    for i, (sys_key, answer, fault, case_dir) in enumerate(cases, 1):
        t0 = time.time()
        ans = answer.replace("-", "").lower()
        source = f"{args.tag}:{sys_key}:{answer}_{fault}:{case_dir.name}"
        events, inject = _load_case(case_dir, valid_by_system[sys_key])
        earliest = min(e["time"] for e in events)
        latest = max(e["time"] for e in events)
        _ship(events, token, source)
        _wait_searchable(source, len(events), earliest, latest)
        ranks = _rank(source, inject, earliest, latest)
        confirmed = (
            _statespace_confirms(source, inject, ranks[0], earliest, latest)
            if args.statespace and ranks
            else None
        )
        top1 = bool(ranks[:1] and ranks[0] == ans)
        rec = {
            "system": sys_key,
            "service": answer,
            "fault": fault,
            "case": case_dir.name,
            "answer": ans,
            "ranks": ranks[:5],
            "ac1": int(ans in ranks[:1]),
            "ac3": int(ans in ranks[:3]),
            "ac5": int(ans in ranks[:5]),
            "statespace_confirms": confirmed,
            "seconds": round(time.time() - t0, 1),
        }
        results.append(rec)
        OUT.mkdir(parents=True, exist_ok=True)
        (OUT / "rcaeval_results.json").write_text(json.dumps(results, indent=2))
        mark = "ok" if top1 else "XX"
        print(
            f"[{i:>3}/{len(cases)}] {sys_key} {answer}_{fault}/{case_dir.name:<3} {mark} "
            f"top1={ranks[0] if ranks else None}  ({rec['seconds']}s)"
        )
        time.sleep(2)  # let the local indexer drain between cases

    _report(results)


def _report(results: list[dict]) -> None:
    n = len(results)
    if not n:
        print("no cases scored")
        return
    ac1 = sum(r["ac1"] for r in results) / n
    ac3 = sum(r["ac3"] for r in results) / n
    ac5 = sum(r["ac5"] for r in results) / n
    # Avg@5 = mean(AC@1..AC@5); AC@2 and AC@4 come from the same ranked list.
    ack = {k: sum(int(r["answer"] in r["ranks"][:k]) for r in results) / n for k in range(1, 6)}
    avg5 = statistics.mean(ack[k] for k in range(1, 6))
    print(f"\n=== kassi vs RCAEval RE3: {n} cases ===")
    print(f"AC@1={ac1:.2%}  AC@3={ac3:.2%}  AC@5={ac5:.2%}  Avg@5={avg5:.2%}")
    print(f"wrote {OUT / 'rcaeval_results.json'}")


if __name__ == "__main__":
    main()
